package Day2;

public class IncrementOperatorDemo {
	
public static void main(String[] args) {
	
	int a = 10;
	a = a +1;
	
	System.out.println(a);//11	
	 
	 
	
//post increment - after the variable value is assigned to the new variable 
	int x=10;
	int result = x++;
// meaning post assignment it is increasing due to the ++ sign
	System.out.println(result);//11 
	System.out.println(x); 

	
	
	
//Pre Increment 
	int b = 20;
	int res = ++b;
		System.out.println(res);//21
	
	
	
	
}
	
	
	
	
	
	
	
	
	
	

}
